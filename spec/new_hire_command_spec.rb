# frozen_string_literal: true

require 'ostruct'
require './lib/employee.rb'
require './lib/new_hire_command.rb'

RSpec.describe NewHireCommand do
  context 'existing team member' do
    before(:all) do
      FileUtils.mkdir_p 'data/teen-titans'
    end

    after(:all) do
      FileUtils.rm_r 'data/teen-titans'
    end

    subject { NewHireCommand.new }

    it 'creates a new team member' do
      expect(File.exist? 'data/teen-titans/princess-koriandr/log.adoc').to be_falsey
      expect(File.exist? 'data/teen-titans/princess-koriandr/overview.adoc').to be_falsey

      expect { subject.command(%w[Teen\ Titans Princess Koriand'r]) }.to output(/princess-koriandr/).to_stdout

      starfire = Employee.find('Princess')
      expect(starfire).not_to be_nil

      expect(starfire.file.path).to eq 'data/teen-titans/princess-koriandr/log.adoc'
      expect(File.exist? 'data/teen-titans/princess-koriandr/log.adoc').to be_truthy
      expect(File.exist? 'data/teen-titans/princess-koriandr/overview.adoc').to be_truthy
      expect(File.read(starfire.file.path)).to include 'File generated by new-hire command'
    end
  end

  context 'force overwrites team member' do
    before(:all) do
      FileUtils.mkdir_p 'data/teen-titans'
    end

    after(:all) do
      FileUtils.rm_r 'data/teen-titans'
    end

    subject { NewHireCommand.new }
    let (:robin) { Employee.find('Grayson') }

    def robin_log_contents
       File.read(robin.file.path)
    end

    def setup_new_hire_with_o3_entry
      expect { subject.command(%w[Teen\ Titans Dick Grayson]) }.to output(/dick-grayson/).to_stdout
      expect(robin).not_to be_nil

      # create a diary entry to differentiate log from a newly created file
      Settings.with_mock_input "\nhere\nMet about goals\n\n\n" do
        RecordDiaryEntryCommand.new.command :one_on_one, ['grayson']
      end

      expect(robin.file.path).to eq 'data/teen-titans/dick-grayson/log.adoc'
      expect(File.exist? 'data/teen-titans/dick-grayson/log.adoc').to be_truthy
      expect(File.exist? 'data/teen-titans/dick-grayson/overview.adoc').to be_truthy
      expect(robin_log_contents).to include 'Met about goals'
    end

    it 'will not recreate an existing team member unless force' do
      setup_new_hire_with_o3_entry
      expect(robin_log_contents).to include 'Met about goals'
      expect { subject.command(%w[Teen\ Titans Dick Grayson]) }.to output(/log.adoc... exists/).to_stdout
      expect(robin_log_contents).to include 'Met about goals'
    end

    it 'forces recreates an existing team member' do
      setup_new_hire_with_o3_entry
      expect(robin_log_contents).to include 'Met about goals'
      expect { subject.command(%w[Teen\ Titans Dick Grayson], OpenStruct.new(force: true)) }.to output(/log.adoc... created/).to_stdout
      expect(robin_log_contents).not_to include 'Met about goals'
    end
  end
end